function initBurgerMenu() {
  const burgerBtn = document.getElementById("burgerBtn");
  const mobileMenu = document.getElementById("mobileMenu");

  burgerBtn.addEventListener("click", () => {
    mobileMenu.classList.toggle("open");
  });

  mobileMenu.querySelectorAll("a").forEach((link) => {
    link.addEventListener("click", () => {
      mobileMenu.classList.remove("open");
    });
  });
}

function initTeamSwitcher() {
  teamSwitchBtn.addEventListener("click", () => {
    teamSwitcher.classList.toggle("open");
  });

  document.querySelectorAll(".team-option").forEach((btn) => {
    btn.addEventListener("click", () => {
      currentTeam = btn.dataset.team;
      updateTeamLabels();
      renderTable();
      renderNextMatch();
      renderCoaches();
      renderSquad();
      teamSwitcher.classList.remove("open");
    });
  });

  document.addEventListener("click", (event) => {
    if (!teamSwitcher.contains(event.target)) {
      teamSwitcher.classList.remove("open");
    }
  });
}

function initRevealAnimations() {
  const elements = document.querySelectorAll(".reveal");

  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("show");
        }
      });
    },
    {
      threshold: 0.15
    }
  );

  elements.forEach((element) => observer.observe(element));
}

function initModalEvents() {
  modalClose.addEventListener("click", closeNewsModal);
  modalBackdrop.addEventListener("click", closeNewsModal);

  coachModalClose.addEventListener("click", closeCoachModal);
  coachModalBackdrop.addEventListener("click", closeCoachModal);

  document.addEventListener("keydown", (event) => {
    if (event.key === "Escape") {
      closeNewsModal();
      closeCoachModal();
    }
  });
}

function initYear() {
  document.getElementById("currentYear").textContent = new Date().getFullYear();
}

function init() {
  initLangButtons();
  initBurgerMenu();
  initTeamSwitcher();
  initRevealAnimations();
  initModalEvents();
  initYear();
  applyTranslations();
}

