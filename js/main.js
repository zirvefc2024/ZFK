function getCurrentClubData() {
  return clubData[currentTeam];
}

function syncTableVisibility() {
  const tableSection = document.getElementById("table");
  const tableLinks = document.querySelectorAll('a[href="#table"]');
  const shouldShowTable = Boolean(siteSettings.showTable);

  if (tableSection) {
    tableSection.hidden = !shouldShowTable;
  }

  tableLinks.forEach((link) => {
    link.style.display = shouldShowTable ? "" : "none";
  });
}

function applyTranslations() {
  document.documentElement.lang = currentLang;
  syncTableVisibility();

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    if (translations[currentLang][key]) {
      element.textContent = translations[currentLang][key];
    }
  });

  document.querySelectorAll(".lang-btn").forEach((btn) => {
    btn.classList.toggle("active", btn.dataset.lang === currentLang);
  });

  updateTeamLabels();
  renderNews();
  renderAchievements();
  renderTable();
  renderNextMatch();
  renderCoaches();
  renderSquad();
}

function updateTeamLabels() {
  const label = getCurrentClubData().label;
  currentTeamLabel.textContent = label;
  heroTeamBadge.textContent = label;
  sectionTeamBadge.textContent = label;
  coachesTeamBadge.textContent = label;

  document.querySelectorAll(".team-option").forEach((btn) => {
    btn.classList.toggle("active", btn.dataset.team === currentTeam);
  });
}

function renderNews() {
  newsList.innerHTML = "";

  newsData.forEach((item, index) => {
    const card = document.createElement("article");
    card.className = "news-card";
    card.style.animationDelay = `${index * 0.08}s`;

    card.innerHTML = `
      <img class="news-card-image" src="${item.image}" alt="${item.title[currentLang]}" />
      <div class="news-card-body">
        <span class="news-card-date">${item.date}</span>
        <h3 class="news-card-title">${item.title[currentLang]}</h3>
        <p class="news-card-text">${item.shortText[currentLang]}</p>
      </div>
    `;

    card.addEventListener("click", () => openNewsModal(item));
    newsList.appendChild(card);
  });
}

